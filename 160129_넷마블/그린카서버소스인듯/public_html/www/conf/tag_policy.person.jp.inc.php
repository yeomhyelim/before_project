<P>当社は、個人情報を取り扱うにあたっては、「個人情報の保護に関する法律」をはじめとする個人情報の保護に関する法令、ガイドラインおよび本プライバシーポリシーを遵守いたします。</P>
<P>&nbsp;</P>
<P>当社は、必要な範囲で個人情報を収集することがあります。収集する個人情報の範囲は、利用目的を達成するために必要な限度を超えないものとします。<BR>また、収集するにあたっては、適正かつ公正な手段により行い、法令により例外として扱うことが認められている場合を除き、利用目的を予め公表するか、または取得後速やかにご本人に通知もしくは公表いたします。<BR>なお、ご本人から書面（インターネット上のWEBサイトの画面等を含みます。）により直接個人情報を収集する場合には、法令により例外として扱うことが認められている場合を除き、その都度予め利用目的を明示いたします。</P>
<P>&nbsp;</P>
<P>・当社が個人情報を取り扱う際には、管理責任者を置き、適切な管理を行うとともに、外部への流出防止に努めます。また、外部からの不正アクセスまたは紛失、破壊、改ざん等の危険に対しては、適切かつ合理的なレベルの安全対策を実施し、個人情報の保護に努めます。<BR>・当社は、個人情報に係るデータベース等へのアクセス権を有する者を限定し、社内においても不正な利用がなされないように厳重に管理いたします。<BR>・当社は、個人情報の取扱いを外部に委託することがあります。この場合、個人情報を適正に取り扱っていると認められる委託先（以下「業務委託先」といいます）を選定し、委託契約等において、個人情報の管理、秘密保持、再提供の禁止等、個人情報の漏洩等なきよう必要な事項を取り決めるとともに、適切な管理を実施させます。<BR>・当社は、個人情報の利用目的が達成された場合は、遅滞なく個人情報を廃棄・消去いたします。</P>
<P>&nbsp;</P>
<P>当社は、ご本人の同意を得た場合、および法令により例外として取り扱うことが認められている場合を除き、収集した個人情報について、収集の際に予め明示した目的または公表している利用目的においてのみ利用いたします。</P>
