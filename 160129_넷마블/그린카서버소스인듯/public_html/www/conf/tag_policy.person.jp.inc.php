<P>当社は、個人情報をご本人の同意なしに、業務委託先以外の第三者に開示・提供することはありません。ただし、法令により開示を求められた場合、または裁判所、警察等の公的機関から開示を求められた場合には、ご本人の同意なく個人情報を開示・提供することがあります。</P>
